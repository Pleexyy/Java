
public class Habitant {
	private String nomHabitant;
	private String nomVillage;
	private String age;
	private String qualite;
	
	
	public Habitant(String nomH, String nomV, String age, String qualite ) {
		this.nomHabitant = nomH;
		this.nomVillage = nomV;
		this.age = age;
		this.qualite = qualite;
	}
	
	public String getNomHabitant() {
		return this.nomHabitant;
	}
	
	public String getNomVillage() {
		return this.nomVillage;
	}
	
	public String getAge() {
		return this.age;
	}
	
	public String getQualite() {
		return this.qualite;
	}


	
	
	
	
	
	
}
